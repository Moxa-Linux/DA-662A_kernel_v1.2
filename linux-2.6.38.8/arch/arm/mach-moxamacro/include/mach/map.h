#define MOXAARM11_PA_PCI_MEM_BASE	0xF8000000

#define MOXAARM11_PA_PCIE_DATA_BASE	0xD0000000
#define MOXAARM11_PA_PCIE_IO_BASE	0xD4000000
#define MOXAARM11_PA_PCIE_MEM_BASE	0xD5000000
